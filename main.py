# [함수 2] 플레이어의 선택을 입력받고 검증하여 데미지를 계산하는 함수
# =====================================================================
def process_choice(stage_data):
    stage_id = stage_data[0]
    stage_name = stage_data[1]
    description = stage_data[2]
    choice1 = stage_data[3]
    choice2 = stage_data[4]
    base_damage = stage_data[5]
    
    print(f"\n📍 현재 위치: {stage_name}")
    print(description)
    print(choice1)
    print(choice2)
    
    # 💡 [예외 처리 무한루프] 1 또는 2를 올바르게 입력할 때까지 반복!
    while True:
        user_input = input("당신의 선택은? (1 또는 2 입력): ")
        if user_input == "1" or user_input == "2":
            break
        else:
            print("❌ 오타 경고: 올바른 행동 번호(1 또는 2)를 입력하세요.")
            
    # 💡 [조건문 부분] 플레이어의 선택에 따른 데미지 판정 알고리즘
    if user_input == "1":
        print("\n[성공] 신중하게 행동하여 피해 없이 구역을 통과했습니다.")
        return 0
    else:
        print("\n💥 [위기] 적의 보안망에 걸리거나 공격을 받았습니다!")
